export function hashString(string, hash)
{
    // implementation of the pearson hash algorithm
    string = string || ""
    hash = hash || 0
    for (let i = 0; i < string.length; i++)
    {
        hash = pearsonPermutation[hash ^ (string.charCodeAt(i) % 256)];
    }
    return hash
}

